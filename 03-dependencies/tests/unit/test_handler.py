def test_lambda_handler_for_json(apigw_json_event):
    """
    Tests that the lambda handler correctly negotiates JSON content,
    responding with a status 200 as well as having the required properties
    in the returned JSON object.
    """
    ret = app.lambda_handler(apigw_json_event, "")
    data = json.loads(ret["body"])

    assert ret["statusCode"] == 200
    assert ret["headers"]["Content-Type"] == "application/json"
    assert "invoke_time" in data


def test_lambda_handler_for_html(apigw_html_event):
    """
    Tests that the lambda handler correctly negotiates HTML content,
    responding with a status 200 and the right content type
    """
    ret = app.lambda_handler(apigw_html_event, "")
    assert ret["statusCode"] == 200
    assert ret["headers"]["Content-Type"] == "text/html"
